/* troca a imagem da estrela da logomarca */
function trocarImagemEstrela() {
    var img = document.querySelector("#cabecalho__esquerda--estrelaMaior");
    img.setAttribute("src", "./assets/images/estrela-cor.png");
}

function voltarImagemEstrela() {
    var img = document.querySelector("#cabecalho__esquerda--estrelaMaior");
    img.setAttribute("src", "./assets/images/estrela.png")
}


